#Operadores aritmeticos
#Uno de los operadores mas importantes es el de asignacion que es un igual
numero1 = 77
numero2 = 44

#Aqui se muestran algunos operadores aritmeticos
resta = numero1 - numero2
multiplicacion = numero1*numero2
division = numero1/numero2
#El resto es algo con lo que se confunden muchos, pero es lo que sobra despues de dividir
resto = numero1 % numero2

#Impresiones
print(f"La resta es: {resta}")
print(f"La suma de los numeros es: {numero1+numero2}")
print(f"La multiplicacion es: {multiplicacion}")
print(f"La division es: {division}")
print(f"El resto es: {resto}")
